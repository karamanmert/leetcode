package blind75.linkedlist.linkedListCycle.Solution;

/**
 * @author karamanmert
 * @date 21.12.2024
 */
public class ListNode {
    int val;
    ListNode next;

    public ListNode() {
    }

    public ListNode(int val) {
        this.val = val;
    }

    public ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
